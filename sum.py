# This program adds two numbers

a = 7
b = 2

# Add two numbers
sum = float(a) + float(b)
# you can also write it as:
sum = float(a + b)

# Display the sum
print("The sum of {0} and {1} is {2}".format(a, b, sum))
# in python 3.6 or above you can write it as
print(f"sum of {a} and {b} is {sum}")
